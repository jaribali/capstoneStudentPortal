function uploadImage() {
    let data = new FormData();
    data.append('file', $('#passportPic')[0].files[0]);

    $.ajax({
        url: '/add_image',
        type: 'POST',
        data: data,
        enctype: 'multipart/form-data',
        processData: false,
        contentType: false,
        success: function (response) {
            if (response.status === "success") {
                uploadFormData(response.photo_id);
            } else {
                alert(response.message || "Upload failed");
            }
        },
        error: function (err) {
            console.log(err);
        }
    });
}

function uploadFormData(photo_id) {
    let result = isValidated();

    if (!result.isValid) {
        alert(`Please fill the following field correctly: ${result.invalidTxt}`);
        return;
    }

    $.ajax({
        url: '/add_details',
        type: 'POST',
        dataType: 'json',
        data: JSON.stringify({
            'firstName': $('#firstName').val().toLowerCase(),
            'middleName': $('#middleName').val().toLowerCase() || "",
            'lastName': $('#lastName').val().toLowerCase(),
            'email': $('#email').val().toLowerCase(),
            'dob': $('#dob').val(),
            'gender': $('input[name="gender"]:checked').val(),
            'phoneNumber': $('#phoneNumber').val(),
            'address': $('#address').val(),
            'regionOfOrigin': $('#regionOfOrigin').val().toLowerCase(),
            'districtOfOrigin': $('#districtOfOrigin').val().toLowerCase(),
            'nextOfKin': $('#nextOfKin').val().toLowerCase(),
            'wassceAggregate': $('#wassceAggregate').val(),
            'status': 'pending',
            'photoId': photo_id
        }),
        contentType: 'application/json; charset=UTF-8',
        success: function (data) {
            alert(data.message);
            location.href = '/admin/dashboard'
        },
        error: function (err) {
            console.log(err);
        }
    });
}

function updateStatus(selectedStatus) {
    let parent = $(this).parents('tr');

    let student_id = $('#student-id').text();
    /* console.log(student_id) */

    $.ajax({
        url: '/student/edit',
        type: 'POST',
        dataType: 'json',
        data: JSON.stringify({
            'id': student_id,
            'status': selectedStatus
        }),
        contentType: 'application/json; charset=UTF-8',
        success: function (data) {
            location.reload();
        },
        error: function (err) {
            console.log(err);
        }
    });
}

function searchStudent() {
    let key = '';
    let source = '';

    if ($('#name-search').val()) {
        key = $('#name-search').val().toLowerCase()
        source = 'name';

    } else if ($('#status-search').val().toLowerCase() !== 'select admission status') {
        key = $('#status-search').val().toLowerCase()
        source = 'status';

    } else if ($('#gender-search').val().toLowerCase() !== 'select gender') {
        key = $('#gender-search').val().toLowerCase()
        source = 'gender';

    } else if ($('#agg-search').val()) {
        key = $('#agg-search').val()
        source = 'agg';
    }

    if (!key || !source) {
        alert("Invalid Search Query");
        return;
    }

    location.href = `/admin/${source}/${key}/search`;
}

function isValidated() {

    let isValid = true;
    let invalidTxt = '';

    $('.form-control, .form-select').removeClass('is-invalid');
    function markInvalid(msg) {
        isValid = false;
        invalidTxt = msg;
    }

    if ($('#firstName').val().trim() === "") markInvalid('First Name');

    if ($('#lastName').val().trim() === "") markInvalid('Last Name');

    if ($('#email').val().trim() === "") {
        markInvalid('Email');
    } else {
        let emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailPattern.test($('#email').val())) {
            markInvalid('Email');
        }
    }

    if ($('#dob').val() === "") markInvalid('Date of Birth');
    if (!$('input[name="gender"]:checked').val()) {
        markInvalid('Gender');
        markInvalid('Gender');
    }

    if ($('#phoneNumber').val().trim() === "") markInvalid('Phone Number');
    if ($('#address').val().trim() === "") markInvalid('Address');

    if ($('#regionOfOrigin').val() === "" ||
        $('#regionOfOrigin').val().toLowerCase() === "select region") {
        markInvalid('Region');
    }
    if ($('#districtOfOrigin').val() === "" ||
        $('#districtOfOrigin').val().toLowerCase() === "select district") {
        markInvalid('District');
    }
    if ($('#nextOfKin').val().trim() === "") markInvalid('Next of Kin');

    let agg = $('#wassceAggregate').val().trim();
    if (agg === "" || isNaN(agg) || agg < 6 || agg > 54) {
        markInvalid('Aggregate');
    }

    return {
        isValid: isValid,
        invalidTxt: invalidTxt
    };
}

function populateRegions() {
    const regions = Object.keys(regionalData.Ghana);

    regions.forEach(region => {
        $('#regionOfOrigin').append(`<option value="${region}">${region}</option>`);
    });

    $('#regionOfOrigin').on('change', function () {

        let selectedRegion = $(this).val();

        $('#districtOfOrigin').html('<option value="">Select District</option>');

        if (selectedRegion && regionalData.Ghana[selectedRegion]) {

            let districts = regionalData.Ghana[selectedRegion].districts;

            districts.forEach(district => {
                $('#districtOfOrigin').append(
                    `<option value="${district}">${district}</option>`
                );
            });
        }
    });
}

window.onload = function () {

    populateRegions();

    $('#student-detail-form').submit(function () {
        uploadImage();
    });

    $('.view-student').click(function () {
        let student_id = 'view-std-' + $(this).attr('id');
        location.href = '/student/' + student_id.replaceAll("view-std-", "") + '/view';
    });

    $('#change-status').on('change', function () {
        let selectedStatus = $(this).val();
        /* console.log(selectedStatus) */
        updateStatus(selectedStatus)
    });

    $('#search').click(function () {
        searchStudent();
    });
}